export function DbIcon({
  name,
  size = 14,
  className,
  style,
}: {
  name: DbIconName
  size?: number
  className?: string
  style?: React.CSSProperties
}) {
  const icon = ICONS[name]
  if (!icon) return null

  return (
    <svg
      width={size}
      height={size}
      viewBox={icon.viewBox ?? "0 0 16 16"}
      fill="none"
      className={className}
      style={style}
      aria-hidden
    >
      {icon.content}
    </svg>
  )
}

